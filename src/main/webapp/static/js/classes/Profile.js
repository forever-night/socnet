function Profile() {
    this.id = 0;
    this.name = "";
    this.dateOfBirth = "";
    this.phone = "";
    this.country = "";
    this.city = "";
    this.info = "";
}

function Profile(id, name, dateOfBirth, phone, country, city, info) {
    this.id = id;
    this.name = name;
    this.dateOfBirth = dateOfBirth;
    this.phone = phone;
    this.country = country;
    this.city = city;
    this.info = info;
}
